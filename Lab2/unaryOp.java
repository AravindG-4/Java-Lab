class unaryOp 
{
    public static void main(String[] args)
    {
        int i=10,j=20;
        System.out.println("Post Increment " + i++);
        System.out.println("After Post Increment " + i);
        System.out.println("Pre Increment " + ++i);
        System.out.println("After Pre Increment " + i);
        System.out.println("Post Decrement " + j--);
        System.out.println("After Post Decrement "+ j);
        System.out.println("Pre Decrement "+ --j);
        System.out.println("After Pre Decrement "+ j);
    }    
}