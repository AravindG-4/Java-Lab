class dataTypes
{
    public static void main(String[] args)
    {
        byte b = 110;
        Boolean B = true;
        short s = 230;
        int i = 34596; 
        char c = '%'; 
        float f = 09.8F;
        double d = 35.5;
        long l = 5000000;
        System.out.println("byte data - " + b);
        System.out.println("boolean data -  " + B);
        System.out.println("short data - " + s);
        System.out.println("int data - " + i);
        System.out.println("character data - " + c);
        System.out.println("float data - " + f);
        System.out.println("double data - " + d);
        System.out.println("long int - " + l);
    }
}